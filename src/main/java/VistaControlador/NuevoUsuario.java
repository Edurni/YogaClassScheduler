    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String usuario = Usuariotx.getText();
        String contrasena = jPasswordField1.getText().toString();
        String confirmacion = jPasswordField2.getText().toString();
        String nombre = Nombretx.getText();
        String apellido = Apellidotx.getText();
        String correo = correotx.getText().trim();// elimina espacios en blanco delante y detras

        Date fechaSeleccionada = jDateChooser1.getDate();
        String fechaComoString = null;

       
            if (fechaSeleccionada != null) {
                SimpleDateFormat formato = new SimpleDateFormat("dd-MM-yyyy");
                fechaComoString = formato.format(fechaSeleccionada);
            }
        

        if (nombre.isEmpty()) {
            nombre = null;
        }
        if (apellido.isEmpty()) {
            apellido = null;
        }

        if (contrasena.equals(confirmacion)) {
            jPasswordField2.setBorder(BorderFactory.createLineBorder(Color.GREEN));
            bd.GuardarUsuario(usuario, contrasena, nombre, apellido, fechaComoString, correo);
             javax.swing.JOptionPane.showMessageDialog(this, "Usuario añadido exito!.", "ENHORABUENA", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            jPasswordField2.setBorder(BorderFactory.createLineBorder(Color.RED));
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    private boolean ValidacionCorreo(String email) {
        // Expresión regular para validar direcciones de correo electrónico
        String emailRegex = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";
        Pattern pattern = Pattern.compile(emailRegex);
        Matcher matcher = pattern.matcher(email);
        return matcher.matches();
    }

