    private void NombretxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NombretxKeyTyped
        //CONTROLO SOLO ENTRAN LETRAS
        char c = evt.getKeyChar();// es utilizado para obtener el carácter correspondiente a una tecla presionad
        if ((c < 'a' | c > 'z') && (c < 'A' | c > 'Z') && (c < ' ' | c > ' '))
            evt.consume();
    }//GEN-LAST:event_NombretxKeyTyped

